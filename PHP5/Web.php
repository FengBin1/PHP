<?php
// 定义一个名为 Web 的类
class Web {
    // 声明一个静态属性 $num，用于记录访客数量，默认为 "1"
    static $num = "1";

    // 声明一个静态方法 change()，用于返回访客数量，并将访客数量递增
    static function change() {
        // 返回当前访客数量的字符串表示，并在递增前加上提示信息
        return "您是本站第" . self::$num . "位访客.\t";

        // 由于上一行已经返回结果并结束方法，以下代码不会执行
        self::$num++; // 递增访客数量
    }
}

// 创建 Web 类的一个实例
$web = new Web();

// 输出提示信息
echo "第一次通过对象调用:<br>";

// 通过实例调用 change() 方法，但由于没有输出结果，因此需要使用 echo 打印
echo $web->change(); // 输出："您是本站第1位访客."
echo $web->change(); // 输出："您是本站第1位访客."（由于之前的方法调用并未增加访客数量，因此仍然是第1位访客）
echo $web->change(); // 输出："您是本站第1位访客."（同上）

// 输出换行符
echo "<br>第二次通过类名调用：<br>";

// 通过类名调用 change() 方法，并打印结果
echo Web::change(); // 输出："您是本站第2位访客."（由于此时是通过类名调用，之前的方法调用并未影响到这里）
echo Web::change(); // 输出："您是本站第3位访客."（递增了访客数量）
?>
